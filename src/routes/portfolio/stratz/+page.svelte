<section>
	<h2>Description</h2>
	<p>STRATZ Esports is an advanced esports analytics platform for Dota 2.</p>
	<h3>Date</h3>
	<p>2019 – 2022</p>
	<h3>Medium</h3>
	<p>Web App, Video</p>
